			if ( ! $old_role || $old_role === $role ) {
				continue;
			}

			* This action is documented in wp-includes/class-wp-user.php 
			do_action( 'remove_user_role', $this->ID, $old_role );
		}

		if ( $role && ! in_array( $role, $old_roles, true ) ) {
			* This action is documented in wp-includes/class-wp-user.php 
			do_action( 'add_user_role', $this->ID, $role );
